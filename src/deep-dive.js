function degreeNumber(item) {
  if (Number.isFinite(Number(item.degree))) return Number(item.degree);
  const match = String(item.degreeLabel || '').match(/\d+(?:[.,]\d+)?/);
  return match ? Number(match[0].replace(',', '.')) : null;
}

function degreeGuide(item) {
  const degree = degreeNumber(item);
  if (degree === null) return 'Точный градус добавляет нюанс, но не используется как шкала «силы» планеты.';
  if (degree < 10) return `Первые 10° знака: качество проявляется более непосредственно и исследует базовую форму ${item.sign}. Это нюанс, а не показатель большей или меньшей силы.`;
  if (degree < 20) return `Средняя часть знака: качество чаще проявляется устойчиво и узнаваемо. Градус уточняет оттенок, но не измеряет «мощность» планеты.`;
  return `Последние 10° знака: качество может проявляться более многослойно и требовать тонкой настройки. Это символический нюанс, а не шкала интенсивности.`;
}

function motionGuide(item) {
  if (item.key === 'northNode') return 'Лунные узлы рассчитываются иначе, поэтому отметка движения здесь не трактуется как ретроградность планеты.';
  if (['sun', 'moon'].includes(item.key)) return 'Для Солнца и Луны ретроградность не применяется.';
  if (item.retrograde) return 'Ретроградность не делает функцию слабой. Она чаще просит сначала переработать импульс внутри, проверить собственную формулировку и только затем выражать его наружу.';
  return 'Директное движение обычно облегчает более прямой перенос функции во внешнее действие, хотя конкретный способ всё равно задают знак, дом и аспекты.';
}

function elementComparison(currentElement) {
  return Object.entries(ELEMENT_GUIDES).map(([name, guide]) => ({
    name,
    current: name === currentElement,
    text: `Эта функция включается ${guide.channel}.`,
  }));
}

export function buildDeepDive(item) {
  const guide = PLANET_GUIDES[item.key];
  if (!guide) throw new Error(`Deep guide is missing ${item.key}`);
  const role = planetKnowledge(item.key);
  const sign = signKnowledge(item.sign);
  const meta = SIGN_META[item.sign];
  if (!meta) throw new Error(`Sign meta is missing ${item.sign}`);
  const area = houseKnowledge(item.house, item.houseArea);
  const areaPhrase = item.house ? `в сфере «${area}»` : 'в нескольких сферах жизни без точной привязки к дому';
  const context = { item, sign: item.sign, element: meta.element, mode: meta.mode, area, areaPhrase };

  return {
    journeyRole: guide.journeyRole,
    headline: guide.question,
    purpose: guide.purpose,
    formula: {
      planet: {
        title: `${item.name}: задача функции`,
        text: `${role.function}.`,
      },
      element: {
        title: `${meta.element}: базовый канал`,
        text: `Эта функция естественнее включается ${ELEMENT_GUIDES[meta.element].channel}. В ресурсе стихия ${ELEMENT_GUIDES[meta.element].resource}; в перегрузе ${ELEMENT_GUIDES[meta.element].stress}.`,
      },
      sign: {
        title: `${item.sign}: личный способ`,
        text: `Знак уточняет канал: функция работает ${sign.mechanism}. Сильная сторона — ${sign.strength}.`,
      },
      house: {
        title: item.house ? `${item.house} дом: место применения` : 'Дом не рассчитан',
        text: item.house
          ? `Особенно заметно это в сфере «${area}». Дом не меняет функцию планеты, а показывает, где вы чаще встречаете её задачи в реальной жизни.`
          : 'Без точного времени HeroStar не приписывает функции конкретную сферу. Смысл планеты, стихии и знака остаётся доступным, но дом не выдумывается.',
      },
      mode: {
        title: `${meta.mode[0].toUpperCase()}${meta.mode.slice(1)} темп`,
        text: MODE_GUIDES[meta.mode],
      },
      degree: {
        title: `${item.degreeLabel || 'Градус'}: дополнительный нюанс`,
        text: degreeGuide(item),
      },
      motion: {
        title: item.retrograde && !['sun', 'moon', 'northNode'].includes(item.key) ? 'Ретроградная переработка' : 'Направление выражения',
        text: motionGuide(item),
      },
    },
    lifeExamples: guide.examples(context).map(([title, text]) => ({ title, text })),
    states: {
      resource: guide.resource(context),
      stress: guide.stress(context),
      return: guide.reset(context),
    },
    elementComparison: elementComparison(meta.element),
    distinguish: guide.distinguish.map(([name, text]) => ({ name, text })),
    practice: {
      title: `Эксперимент: ${guide.journeyRole.toLowerCase()}`,
      steps: guide.practice(context),
    },
    note: 'Используйте описание как гипотезу для самонаблюдения. Проверяйте его на знакомых ситуациях и меняйте вывод, если реальный опыт показывает другое.',
  };
}
