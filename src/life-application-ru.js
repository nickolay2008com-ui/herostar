function fallbackHouseApplication(house, fallbackArea) {
  const area = fallbackArea || `${house} дома`;
  return {
    context: `в ситуациях, связанных с темой «${area}»`,
    examples: [
      'заметить повторяющуюся ситуацию',
      'выбрать в ней один понятный результат',
      'проверить свой способ небольшим действием',
    ],
  };
}

export function buildLifeApplication({ planetKey, sign, house, fallbackArea = null }) {
  if (!house) return null;

  const planetApplication = PLANET_APPLICATIONS[planetKey] || 'использовать эту внутреннюю силу';
  const signApplication = SIGN_APPLICATIONS[sign] || 'действовать своим естественным способом';
  const houseApplication = HOUSE_APPLICATIONS[house] || fallbackHouseApplication(house, fallbackArea);

  return [
    `Здесь эта сила помогает ${planetApplication} ${houseApplication.context}.`,
    `Ваш рабочий способ — ${signApplication}.`,
    `Например: ${houseApplication.examples.join('; ')}.`,
  ].join(' ');
}
